#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	al;

	al = (unsigned char)c;
	if ((al >= 'A' && al <= 'Z') || (al >= 'a' && al <= 'z'))
		return (1);
	return (0);
}
